/**
* @brief Function implementing the usbCdc thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_usbCdcTask */
void usbCdcTask(void const * argument)
{
  /* USER CODE BEGIN usbCdcTask */
	osEvent event;
	canRxMessage *canMessage;
	uint8_t buf[100];
	uint8_t i = 0;;

	buf[0] = 0xAA; buf[1] = 0x55;
	buf[2] = 0xAA; buf[3] = 0x55;

  /* Infinite loop */
  for(;;)
  {
	event = osMailGet(canRxQueueHandle, osWaitForever);
		if (event.status == osEventMail){
			canMessage = event.value.p;

			i = 4;
			buf[i++] = (canMessage->header->StdId & 0x000000FF);
			buf[i++] = (canMessage->header->StdId & 0x0000FF00) >> 8;
			buf[i++] = (canMessage->header->StdId & 0x00FF0000) >> 16;
			buf[i++] = (canMessage->header->StdId & 0xFF000000) >> 24;
			buf[i++] = (uint8_t)(canMessage->header->DLC);
			for (i = 9; i < 17; i++) {
				buf[i] = canMessage->data[i-9];
			}

			USBD_CDC_SetTxBuffer(&hUsbDeviceFS, buf, 17);
			USBD_CDC_TransmitPacket(&hUsbDeviceFS);

			osMailFree(canRxQueueHandle, canMessage);
			osThreadYield();
		}
	  }

  /* USER CODE END usbCdcTask */
}

